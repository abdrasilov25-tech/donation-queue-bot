async function handleFaq(ctx) {
  const text =
    `❓ *Частые вопросы*\n` +
    `${'━'.repeat(28)}\n\n` +
    FAQ.map((f) => `*${f.q}*\n${f.a}`).join('\n\n─────────────\n\n') +
    `\n\n${'━'.repeat(28)}\n` +
    `Остались вопросы? Напишите администратору.`;

  await ctx.reply(text, { parse_mode: 'Markdown' });
}

